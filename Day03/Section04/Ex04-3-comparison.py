'''
파일명 : Ex04-3-comparison.py

관계 연산자
    ex) >, >=, <, <=, ==, !=
    2개 항을 비교하여 그 결과를 논리(bool) 자료형으로 반환

'''

a=15
print('{} > 10 : {}'.format(a, a > 10))
print('{} > 10 : {}'.format(a, a < 10))
print('{} > 10 : {}'.format(a, a >= 10))
print('{} > 10 : {}'.format(a, a <= 10))
print('{} > 10 : {}'.format(a, a == 10))
print('{} > 10 : {}'.format(a, a != 10))
# a>10이 맞다면 true 아니면 false
# bool 은 true, false 의 두개의 값만 가지고 있음


